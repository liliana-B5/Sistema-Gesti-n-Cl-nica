    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String cedula = txtCedula.getText();
    String nombre = txtNombrePersona.getText();
    String apellido = txtApellido.getText();
    int edad = Integer.parseInt(txtEdad.getText());
    LocalDate fechaNac = dtcFechaNac.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
    String rol = (String) cmbRol.getSelectedItem();
    String contrasena = txtContrase単a.getText();

    Persona nuevaPersona;
    switch (rol) {
        case "administrador":
            nuevaPersona = new Administrador(nombre, apellido, cedula, edad, fechaNac);
            break;
        case "cajero":
            nuevaPersona = new Cajero(nombre, apellido, cedula, edad, fechaNac);
            break;
        case "medico":
            nuevaPersona = new Medico(nombre, apellido, cedula, edad, fechaNac);
            break;
        default:
            JOptionPane.showMessageDialog(this, "Rol no v叩lido");
            return;
    }

    boolean personaRegistrada = controller.registrarPersona(nuevaPersona);
    boolean usuarioRegistrado = controller.registrarUsuario(cedula, contrasena);

    if (personaRegistrada && usuarioRegistrado) {
        JOptionPane.showMessageDialog(this, " usuario registrados exitosamente");
    } else {
        JOptionPane.showMessageDialog(this, "Error al registrar persona o usuario");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

