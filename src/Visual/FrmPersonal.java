    private void jlistpMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlistpMouseClicked
      // Verifica si el clic fue en un ítem de la lista
    if (evt.getClickCount() == 1) {
        int index = jlistp.locationToIndex(evt.getPoint());
        if (index >= 0) {
            DefaultListModel<String> model = (DefaultListModel<String>) jlistp.getModel();
            String selectedItem = model.getElementAt(index);
            
            String[] parts = selectedItem.split(", ");
            String cedula = parts[0].split(": ")[1];
            String nombre = parts[1].split(": ")[1];
            String apellido = parts[2].split(": ")[1];
            int edad = Integer.parseInt(parts[3].split(": ")[1]);
            LocalDate fechaNac = LocalDate.parse(parts[4].split(": ")[1]);

            // Carga los datos en los campos de texto
            txtCedula.setText(cedula);
            txtNombre.setText(nombre);
            txtApellido.setText(apellido);
            txtEdad.setText(String.valueOf(edad));
            dtcFecha.setDate(java.sql.Date.valueOf(fechaNac));
            
        }
        }
    }//GEN-LAST:event_jlistpMouseClicked

    private void btnbuscarpersonalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarpersonalActionPerformed
        String rol = jComboBox1.getSelectedItem().toString().toLowerCase();

        System.out.println("Rol seleccionado: " + rol);

        List<Persona> personas = controller.obtenerPersonasPorRol(rol); // Obtener los datos de la bd

        // Verifica que la lista de personas no sea null
        if (personas == null) {
            System.out.println("La lista de personas es nula");
        } else {
            System.out.println("Cantidad de personas encontradas: " + personas.size());
        }

        // Verifica que jlistp está inicializado
        if (jlistp == null) {
            System.out.println("jlistp no ha sido inicializado");
        } else {
            actualizarLista(personas); // Actualizar la lista con los datos 
        }

    }//GEN-LAST:event_btnbuscarpersonalActionPerformed

