    private void btnGuadarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuadarActionPerformed
     // Obtén los datos de los campos de texto
        String cedula = txtCedula.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        int edad = Integer.parseInt(txtEdad.getText());
        LocalDate fechaNac = dtcFecha.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();

        Medico medico = new Medico(nombre, apellido, cedula, edad, fechaNac); 

        // objeto Medico y actualiza los datos en la base de datos
         boolean exito =controller.actualizarPersona(medico); 

        // Limpia los campos 
        limpiar();
        if (exito) {
            JOptionPane.showMessageDialog(this, "Personal de la clinica actualizada correctamente.", "Éxito", 
                    JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo actualizar el personal.", "Error", 
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuadarActionPerformed
    
