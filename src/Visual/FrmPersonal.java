     // Método para actualizar la lista en el JList
    private void actualizarLista(List<Persona> personas) {
        if (jlistp.getModel() instanceof DefaultListModel) {
            DefaultListModel<String> model = (DefaultListModel<String>) jlistp.getModel();
            model.clear(); // Limpiar la lista

            for (Persona persona : personas) {
                String item = String.format("Cedula: %s, Nombre: %s, Apellido: %s, "
                        + "Edad: %d, Fecha de Nacimiento: %s",
                    persona.getCi(), persona.getNombre(), persona.getApellido(), 
                    persona.getEdad(), persona.getFechanac().toString());
                model.addElement(item);
            }
        } else {
            System.err.println("ERrror al actualizar JList.");
        }
    }

    
